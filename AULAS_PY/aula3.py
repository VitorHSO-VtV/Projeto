"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

print(1234) # Número inteiro

# Aspas simples ''

print('Vitor Henrique') # String com aspas simples

# Aspas duplas ""

print("Vitor Henrique") # String com aspas duplas

# Para usar aspas simples ou duplas dentro de um texto usa-se um caractere de escape
# O caractere de escape no python é \

print("Vitor \"Henrique\"") # String com caracteres de escape

# Usá-se r para mostrar os caracteres de escape

print(r"Vitor \"Henrique\"")

# Um truque para usar aspas simples ou duplas no python é revezar entre elas

print("Vitor 'Henrique'")
print('Vitor "Henrique"')